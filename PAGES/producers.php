<p class="blueuppercase"><strong>About Scott Holmquist</strong></p>

<p>Scott is a researcher, designer and writer who lived from 2007-10 in Humboldt County, where, working with Robert Arena and Joseph Massey, among many others, he produced the <a href="http://chronicfreedombooks.com/" target="_blank"> <i>chronic freedom</i> series</a> of books concerned with Southern Humboldt marijuana life. </p>

<p>The series is recognized as ground breaking by local authorities on SoHum history, such as writer Ray Raphael, state history; Steven Black, Head of the Acquisitions Division at UC Berkeley’s Bancroft Library; and nationally by Mark Dimunation, Chief of the US Library of Congress’s Rare Book and Special Collections Division.</p> 
