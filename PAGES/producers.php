<h2>Producers</h2>
<p> <span class="blueuppercase">Scott Holmquist</span> - Director and designer.</p>

<p> <span class="blueuppercase">Robert Arena</span> - Production manager.</p>

<p>Robert is the owner and operator of <a href="http://bugpress.com/" target="_blank">Bug Press, Inc.</a>, which has received numerous awards for excellence in printing, including a Certificate of Merit from Printing Industries of America (2010), a Gold Award from the National Association of Litho Clubs (2010), and Gold, Silver and Bronze awards from the Visual Media Alliance Showcase of Print Design/Excellence (2006, 2007, 2010).</p>

<p>He managed the production of the award-winning book <a target="_blank" href="http://www.humboldtheartland.com/index.html"><i>Humboldt Heartland</i></a> and will oversee production of <i>Low Tide</i>.</p>

<p> <span class="blueuppercase">Joseph Massey</span> - Writer, editor.</p>

<p> Joseph is author of two books of poetry, <a href="http://www.shearsman.com/pages/books/catalog/2009/massey.html" target="_blank"><i>Areas of Fog</i></a> (Shearsman Books, 2009) and <a href="http://www.shearsman.com/pages/books/catalog/2011/massey2011.html" target="_blank"><i>At the Point</i></a> (Shearsman Books, 2011), as well as over a dozen fine press, limited edition chapbooks and broadsides.</p>
 
<p>Much of his work springs from observations of the Bay.</p>

<p> <span class="blueuppercase">Robin Pagliuco</span> - Project manager.</p>

