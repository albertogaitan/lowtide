<h2>About</h2>
<div style="float: left; width: 400px;">

<p><strong><i>Low Tide</i></strong> is an <span class="blueuppercase">Art and Archive Project</span> assembling a comprehensive survey of visual Bay histories for public re-presentation and preservation.</p>

<p class="blueuppercase"><strong>COLLECTING AND RADICAL ARCHIVING</strong></p>

<p>Working mainly from our office donated by Bug Press in Arcata, CA, we are collecting material on Humboldt Bay to reproduce in exhibitions, books, and archives, incorporating photos, charts, maps and other graphics.</p>

<p>Our aim is to include the entirety of every document published to date, within evolving limits. We began in 2011 and expect to continue at least through 2015.</p>

<p>We also aim to design, or incite the design of, a new form of fully analog index. Using micro re-printing and expandable binding, we counter trends to replace printed catalogs and indexes with exclusively digital ones.</p>

<p>People from Bay Keepers to the Yacht Club and the Humboldt County Historical Society are helping us gather and sort materials. (See <a href="#support">Support</a>.)</p>

<p>Many documents have been, and more will be, lost without this effort. For instance, an extensive documentation on the dolosse reinforcement of Bay jetties we received from the Eureka office of California Sea Grant in June 2012. Taking </p>

