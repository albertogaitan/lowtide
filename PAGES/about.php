<p>these scattered documents from offices and special collections to make them viewable together not only assures that some will survive for future enjoyment and study, but it opens new ways to see and interpret the Bay.</p>

<p>Once complete, this visual Bay archive is intended for public use.</p>

<p class="blueuppercase"><strong>REPRINTING AND EXHIBITION</strong></p>

<p>From the archive, we are producing art works, exhibitions and books. The first exhibition took place in Eureka, CA, opening December 13th, 2012. (See <a href="http://lowtidebook.com/exhibits.php">Exhibits</a>.)</p>

<p>Through mostly visual presentations&mdash;wide-ranging and immersive&mdash;these works focus on Humboldt Bay waters.</p>

<p>To our knowledge, no such treatment of any Bay currently exists. And in the case of Humboldt Bay, there is not even the simplest picture book available for serious reference or as a souvenir for visitors.</p>

<p>Though the reproduction in print, and other media, digital-like information densities, we strive to assemble the most robust types of archive and image platforms now possible. Ones that can't be erased by a thunderstorm, ones that may be encountered directly in space and that may be referenced over decades without ever making a software or hardware update.</p>

