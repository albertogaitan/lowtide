    <div id="header_2">"50 x 50 - object index draft 1" Aug 2013</div>
    <iframe src="//player.vimeo.com/video/74629155" width="660" height="281" frameborder="0"></iframe>
    An accordion book, a total of 60 feet each side, by one and a half feet high, containing the first fifty "objects" in the Low Tide collection comprised of fifty or fewer "images," each arranged within a <a href="exhibit_50x50.php"><span style="text-decoration:underline;">single page-spread</span></a>. This object index draft is the first research towards an art of digital to analog production. Accompanying the book were eighteen foam-core mounted spreads from its pages. The work debuted at Humboldt Baykeeper for its August Arts Alive show along with Baykeeper's house band, Kenny Ray and the Mighty Rove's.
    </p>
