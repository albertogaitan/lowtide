    <div id="header_1">"Sample Strips - Low Tide exhibit 1" Dec 2012 - Mar 2013</div>
    <iframe src="//player.vimeo.com/video/69179884" width="660" height="281" frameborder="0"></iframe>
    Introduction to Low Tide work made with hi-res inkjet prints, by Bug Press, on 2 foot wide strips of 5 mil / 32 lb Epson Singleweight Matte paper, hung thoughout the street-level space, floor to ceiling. Each of these main 2x15 foot strips contain six figures reproducing a page or page-spread from a collected document &mdash; or objects. The fourth figure from the top of each strip is the focus aligned with others focus images in a horizontal array from strip to strip. The space was lit nightly on a stretch of Highway 101 that becomes 5th Street, in Eureka.</p>
